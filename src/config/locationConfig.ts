export const getAllStates = (): string[] => {
  return INDIAN_STATES_CITIES.map(item => item.state).sort();
};

export const getCitiesByState = (state: string): string[] => {
  const stateData = INDIAN_STATES_CITIES.find(item => item.state === state);
  return stateData ? stateData.cities.sort() : [];
};

export const getAllCities = (): string[] => {
  const allCities: string[] = [];
  INDIAN_STATES_CITIES.forEach(item => {
    allCities.push(...item.cities);
  });
  return allCities.sort();
};